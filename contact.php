<?php
// Проверяем, что данные были отправлены методом POST
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Получаем данные из формы
    $name = $_POST['name'];
    $email = $_POST['email'];
    $message = $_POST['message'];

    // Проверяем данные на наличие спама
    if (!empty($name) && !empty($email) && !empty($message)) {
        // Формируем строку для отправки сообщения
        $subject = 'Новое сообщение с вашего сайта';
        $headers = 'From: ' . $email . "\r\n";
        $headers .= 'Reply-To: ' . $email . "\r\n";

        // Отправляем сообщение
        mail($email, $subject, $message, $headers);

        // Выводим сообщение об успешной отправке
        echo 'Ваше сообщение было успешно отправлено. Спасибо за обращение!';
    } else {
        // Выводим сообщение об ошибке
        echo 'Некоторые поля формы не были заполнены. Пожалуйста, проверьте и заполните все поля.';
    }
}
?>
